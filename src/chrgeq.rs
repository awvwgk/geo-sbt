pub use crate::elements::*;

pub struct charge_parameter {
    pub en    : f64,
    pub gam   : f64,
    pub kappa : f64,
    pub alpha : f64,
}

/*
 * Taken from:
   E. Caldeweyher, S. Ehlert, A. Hansen, H. Neugebauer, S. Spicher,
   C. Bannwarth and S. Grimme, ChemRxiv, 2019, preprint.
   https://doi.org/10.26434/chemrxiv.7430216.v2
 */
pub fn get_charge_parameter(at : &Elem)
    -> charge_parameter
{
    let en    =    en2019(&at);
    let gam   =   gam2019(&at);
    let kappa = kappa2019(&at);
    let alpha = alpha2019(&at);
    charge_parameter{en:en,gam:gam,kappa:kappa,alpha:alpha}
}

